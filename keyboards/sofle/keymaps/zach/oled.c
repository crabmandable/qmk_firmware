static int round_up(int number_to_round, int multiple)
{
    if (multiple == 0)
        return number_to_round;

    int remainder = number_to_round % multiple;
    if (remainder == 0)
        return number_to_round;

    return number_to_round + multiple - remainder;
}

static void glitch_bit_shift(void) {
    // bit shift
    uint16_t idx = rand() % SCREEN_SIZE;
    uint8_t shift = rand() % 8;
    oled_buffer_reader_t reader = oled_read_raw(idx);
    for (int i = 0; rand() % 10 && i < reader.remaining_element_count; i++){
        char newByte = *reader.current_element++;
        newByte = newByte >> shift;
        oled_write_raw_byte(newByte, idx);
    }
}

static void glitch_swap_chunks(void)
{
    uint8_t size = rand() % 32;
    int offset = rand() % 12 + 4;
    uint16_t idx1 = round_up(rand() % (SCREEN_SIZE - size), 16);
    idx1 = idx1 > SCREEN_SIZE ? 0 : idx1;
    uint16_t idx2 = idx1 + offset;
    idx2 = idx2 > SCREEN_SIZE - size ? 0 : idx2;
    oled_buffer_reader_t reader = oled_read_raw(0);
    for (int i = 0; i < size; i++){
        char b1 = *(reader.current_element + idx1 + i);
        char b2 = *(reader.current_element + idx2 + i);
        oled_write_raw_byte(b2, idx1 + i);
        oled_write_raw_byte(b1, idx2 + i);
    }
}

static void glitch_clean_chunk(void)
{
    uint8_t size = rand() % 32;
    uint16_t idx = round_up(rand() % (SCREEN_SIZE - size), 16);
    idx = idx > SCREEN_SIZE ? 0 : idx;
    oled_set_cursor_idx(idx);
    oled_write_raw_P(logo[logo_idx] + idx, size);
}

static void glitch_clear_chunks(uint8_t minSize, uint8_t maxSize) {
    uint8_t nVals = (rand() % (maxSize - minSize)) + minSize;
    uint16_t idx = rand() % (SCREEN_SIZE - nVals);
    for (int i = 0; i < nVals; i++) {
        oled_write_raw_byte(0x00, idx + i);
    }
}


