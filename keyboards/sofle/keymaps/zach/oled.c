static uint16_t logo_timer;
static uint16_t ripple_timer;

static int round_up(int number_to_round, int multiple)
{
    if (multiple == 0)
        return number_to_round;

    int remainder = number_to_round % multiple;
    if (remainder == 0)
        return number_to_round;

    return number_to_round + multiple - remainder;
}

static void glitch_bit_shift(void) {
    // bit shift
    uint16_t idx = rand() % SCREEN_SIZE;
    uint8_t shift = rand() % 8;
    oled_buffer_reader_t reader = oled_read_raw(idx);
    for (int i = 0; rand() % 10 && i < reader.remaining_element_count; i++){
        char newByte = *reader.current_element++;
        newByte = newByte >> shift;
        oled_write_raw_byte(newByte, idx);
    }
}

static void glitch_swap_chunks(void)
{
    uint8_t size = rand() % 32;
    int offset = rand() % 12 + 4;
    uint16_t idx1 = round_up(rand() % (SCREEN_SIZE - size), 16);
    idx1 = idx1 > SCREEN_SIZE ? 0 : idx1;
    uint16_t idx2 = idx1 + offset;
    idx2 = idx2 > SCREEN_SIZE - size ? 0 : idx2;
    oled_buffer_reader_t reader = oled_read_raw(0);
    for (int i = 0; i < size; i++){
        char b1 = *(reader.current_element + idx1 + i);
        char b2 = *(reader.current_element + idx2 + i);
        oled_write_raw_byte(b2, idx1 + i);
        oled_write_raw_byte(b1, idx2 + i);
    }
}

static void glitch_clean_chunk(void)
{
    uint8_t size = rand() % 32;
    uint16_t idx = round_up(rand() % (SCREEN_SIZE - size), 16);
    idx = idx > SCREEN_SIZE ? 0 : idx;
    oled_set_cursor_idx(idx);
    oled_write_raw_P(crab + idx, size);
}

static void glitch_clear_chunks(uint8_t minSize, uint8_t maxSize) {
    uint8_t nVals = (rand() % (maxSize - minSize)) + minSize;
    uint16_t idx = rand() % (SCREEN_SIZE - nVals);
    for (int i = 0; i < nVals; i++) { 
        oled_write_raw_byte(0x00, idx + i);
    }
}


static void glitch_light(void) {
    if (rand() % 2) {
        glitch_bit_shift();
        glitch_bit_shift();
        glitch_bit_shift();
    }

    if (0 == (rand() % 4)) {
        glitch_swap_chunks();
    }

    if (0 == (rand() % 4)) {
        glitch_clean_chunk();
    }
}

static void glitch_heavy(void) {
    glitch_swap_chunks();

    if (rand() % 2) {
        glitch_clear_chunks(2, 8);
    }
}

static void glitch_fade(void) {
    glitch_clear_chunks(10, 40);
    if (0 == (rand() % 4)) {
        glitch_clean_chunk();
    }
}

static void glitch_fade_more(void) {
    glitch_clear_chunks(20, 40);
}

static void glitch_ripple(void) {
    if ((rand() % 3) == 0) {
        glitch_swap_chunks();
    }
    if (rand() % 2) {
        glitch_clean_chunk();
    }
}

//ripple
#define RIPPLE_PERIOD 4000
#define RIPPLE_LENGTH 300

// end
#define GLITCH_LIGHT 11800
#define GLITCH_HEAVY 12200
#define RENDER_GLITCH_FADE 12600
#define RENDER_GLITCH_FADE_MORE 12800
#define RENDER_CLEAN 13000

void render_logo(void) {
    uint16_t main_time = timer_elapsed(logo_timer);
    uint16_t ripple_time = timer_elapsed(ripple_timer);

    if (main_time < 100) {
        oled_write_raw_P(crab, SCREEN_SIZE);
    }

    if (main_time < GLITCH_LIGHT) {
        if (ripple_time >= RIPPLE_PERIOD && ripple_time < RIPPLE_PERIOD + RIPPLE_LENGTH) {
            glitch_ripple();
        }

        if (ripple_time >= RIPPLE_PERIOD + RIPPLE_LENGTH) {
            oled_write_raw_P(crab, SCREEN_SIZE);
            ripple_timer = timer_read();
        }
    }

    if (main_time >= GLITCH_LIGHT && main_time < GLITCH_HEAVY) {
        glitch_light();
    }

    if (main_time >= GLITCH_LIGHT && main_time < GLITCH_HEAVY) {
        glitch_light();
    }

    if (main_time >= GLITCH_HEAVY && main_time < RENDER_GLITCH_FADE) {
        glitch_heavy();
    }

    if (main_time >= RENDER_GLITCH_FADE && main_time < RENDER_GLITCH_FADE_MORE) {
        glitch_fade();
    }

    if (main_time >= RENDER_GLITCH_FADE_MORE && main_time < RENDER_CLEAN) {
        glitch_fade_more();
    }

    if (main_time >= RENDER_CLEAN) {
        logo_timer = timer_read();
    }
}

static void print_status_narrow(void) {
    // Print current mode
    oled_write_P(PSTR("\n\n"), false);

    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_ln_P(PSTR("Qwrt"), false);
            break;
        case 1:
            oled_write_ln_P(PSTR("Fn"), false);
            break;
        case 2:
            oled_write_ln_P(PSTR("Li"), false);
            break;
        default:
            oled_write_P(PSTR("Mod\n"), false);
            break;
    }
    oled_write_P(PSTR("\n\n"), false);
    // Print current layer
    oled_write_ln_P(PSTR("LAYER"), false);
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("L0"), false);
            break;
        case 1:
            oled_write_P(PSTR("L1"), false);
            break;
        case 2:
            oled_write_P(PSTR("L2"), false);
            break;
        case 3:
            oled_write_P(PSTR("L3"), false);
            break;
        default:
            oled_write_ln_P(PSTR("Undef"), false);
    }
    oled_write_P(PSTR("\n\n"), false);
    led_t led_usb_state = host_keyboard_led_state();
    oled_write_ln_P(PSTR("CPSLK"), led_usb_state.caps_lock);
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
    return OLED_ROTATION_270;
}

void oled_task_user(void) {
    if (is_keyboard_master()) {
        print_status_narrow();
    } else {
        render_logo();
    }
}

